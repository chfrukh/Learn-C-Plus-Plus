#include <iostream>
using namespace std;

int main() {
    int i = 1; // Initialize the loop control variable

    // Use a while loop to print numbers from 1 to 10
    while (i <= 10) {
        cout << i << " ";
        i++; // Increment the loop control variable
    }

    cout << endl; // Print a newline to separate the output

    return 0;
}
